from turtle import *
import math
global a
a = 7
def kwadrat():
    pd()
    fillcolor("darkgreen")
    begin_fill()
    setheading(0)
    fd(2*a)
    lt(90)
    fd(2*a)
    lt(90)
    fd(2*a)
    lt(90)
    fd(2*a)
    end_fill()

def deska(d):
    if d == 1:
        x = 0
    else:
        x = 180
    setheading(x)
    fillcolor("darkgreen")
    begin_fill()
    fd(2*a)
    lt(90)
    fd(2*a)
    lt(45)
    fd(a*math.sqrt(2))
    lt(90)
    fd(a*math.sqrt(2))
    lt(45)
    fd(2*a)
    end_fill()
    pu()
    lt(90)

def rubin():
    fillcolor("darkgreen")
    begin_fill()
    fd(2*a*math.sqrt(2))
    lt(45)
    fd(2*a)
    lt(90)
    fd(2*a)
    lt(45)
    fd(2*a*math.sqrt(2))
    lt(45)
    fd(2*a)
    lt(90)
    fd(2*a)
    end_fill()
    
