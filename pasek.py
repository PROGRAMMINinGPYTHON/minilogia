    for i in range(3):
        kwadrat()
        setheading(0)
        pu()
        fd(3*a)
        pd()
    deska(1)
    pu()
    lt(90)
    fd(2*a)
    lt(45)
    fd(a*math.sqrt(2))
    rt(90)
    pd()
    rubin()
    lt(45)
    fd(2*a*math.sqrt(2))
    lt(45)
    fd(4*a)
    lt(45)
    rubin()
    lt(45)
    fd(2*a*math.sqrt(2))
