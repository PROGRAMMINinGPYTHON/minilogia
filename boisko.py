from turtle import *
global a
a = 350

def pol(n):
    pu()
    setpos(0,0)
    setheading(90)
    pd()
    fillcolor("green")
    begin_fill()
    fd(a/2)
    n(90)
    fd(a)
    n(90)
    fd(a)
    n(90)
    fd(a)
    n(90)
    fd(a)
    end_fill()
    
def zawodnik(kolor,x,y):
    pu()
    setpos(x,y)
    setheading(0)
    pd()
    fillcolor(kolor)
    begin_fill()
    for i in range(6):
        fd(20)
        lt(60)
    end_fill()
    
def druzyna(kolor,x):
    pu()
    zawodnik(kolor,x,100)
    zawodnik(kolor,x,-100)
    zawodnik(kolor,x,0)
    pu()
    
def srodek():
    pu()
    setpos(a/8,0)
    pd()
    circle(a/8)
    
def bramka(n,poz,y):
    color("black")
    setpos(-poz,y-a/4)
    pd()
    fillcolor("yellow")
    begin_fill()
    circle(a/4)
    end_fill()
    pd()
    color("white")
    fillcolor("white")
    begin_fill()
    fd(200)
    n(90)
    fd(200)
    n(90)
    fd(200)
    n(90)
    fd(200)
    end_fill()
    
def linie():
    bramka(rt,a+a/4,a/4)
    pu()
    setpos(a,-a/2)
    setheading(90)
    pd()
    color("black")
    fd(350)
    pu()
    setpos(-a,-a/2)
    setheading(90)
    fd(50)
    fillcolor("white")
    begin_fill()
    lt(90)
    fd(200)
    rt(90)
    fd(400)
    rt(90)
    fd(200)
    rt(90)
    fd(400)
    end_fill()
    rt(180)
    pd()
    color("black")
    fd(250)
    
def pole_karne(poz):
    pu()
    setpos(poz,0)
    begin_fill()
    fillcolor("orange")
    setheading(90)
    pd()
    fd(a/8)
    rt(90)
    fd(a/8)
    rt(90)
    fd(a/4)
    rt(90)
    fd(a/8)
    rt(90)
    fd(a/8)
    end_fill()

def BOISKO():
    pol(lt)
    pol(rt)
    srodek()
    druzyna("red",+150)
    druzyna("blue",-200)
    bramka(lt,-a,-0)
    pu()
    linie()
    pole_karne(-a)
    pole_karne(a-a/8)

BOISKO()
