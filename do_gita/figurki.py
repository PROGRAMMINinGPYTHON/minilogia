from turtle import *
def figura(x):
    pu()
    setpos(-1*x,0)
    pd()
    setpos(0,2*x)
    setpos(4*x,0)
    setpos(0,-3*x)
    setpos(-1*x,0)
    pu()
def gwiazdka(x,y,a):
    pu()
    setpos(x-2*a,y+0)
    pd()
    setpos(x-1*a,y+4*a)
    setpos(x+2*a,y-1*a)
    setpos(x-0*a,y+0*a)
    setpos(x-2*a,y+0)
figura(10)
figura(20)
figura(30)
figura(40)
gwiazdka(40,80,10)
gwiazdka(130,30,10)
gwiazdka(-50,60,10)
gwiazdka(-70,50,10)
gwiazdka(-60,-50,10)
gwiazdka(-50,-100,10)
