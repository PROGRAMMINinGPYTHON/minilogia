from turtle import *
import math
def OKIENKO(a):
    pu()
    setpos((-3*a*math.sqrt(2)-1*a)/2,(-3*a*math.sqrt(2)-1*a)/2)
    pd()
    setheading(0)
    pd()
    fillcolor("darkblue")
    begin_fill()
    fd(a+3*a*math.sqrt(2))
    lt(90)
    fd(a+3*a*math.sqrt(2))
    lt(90)
    fd(a+3*math.sqrt(2)*a)
    lt(90)
    fd(a+3*a*math.sqrt(2))
    end_fill()
    setheading(45)
    pu()
    fd(a/2*math.sqrt(2))
    pd()
    setheading(90)
    fillcolor("skyblue")
    begin_fill()
    fd(3*a*math.sqrt(2))
    rt(90)
    fd(3*a*math.sqrt(2))
    rt(90)
    fd(3*a*math.sqrt(2))
    rt(90)
    fd(3*a*math.sqrt(2))
    end_fill()
    pu()
    setheading(45)
    fd(a/2)
    pd()
    fillcolor("darkblue")
    begin_fill()
    lt(90)
    fd(a/2)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a/2)
    end_fill()
speed(0)    
OKIENKO(60)
