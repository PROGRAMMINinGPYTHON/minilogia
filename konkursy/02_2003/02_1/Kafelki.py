from turtle import*
global a
a = 20
def wzor(x,y):
    setpos(x-5*a,y-6*a)
    setheading(90)
    begin_fill()
    fillcolor("white")
    pd()
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    end_fill()

def kwadrat():
    pu()
    fd(6*a)
    pu()
    lt(90)
    pd()
    fillcolor("blue")
    begin_fill()
    fd(6*a)
    lt(90)
    fd(12*a)
    lt(90)
    fd(12*a)
    lt(90)
    fd(12*a)
    lt(90)
    fd(6*a)
    end_fill()
def KAFELKI(x,y):
    kwadrat()
    pu()
    setpos(0,0)
    pu()
    wzor(x-0*a,y-0*a)
    pu()
    wzor(x,y+4*a)
    pu()
    wzor(x,y+8*a)
    pu()
    setpos(0,0)
    pu()
    for i in range(3):
        wzor(x+4*a*i,y-0*a)
        pu()
        wzor(x+4*a*i,y+4*a*i)
        pu()
        wzor(x+4*a*i,y+8*a)
        pu()
        wzor(x+8*a,y+4*a)
        pu()

speed(0)           
KAFELKI(0,0)
