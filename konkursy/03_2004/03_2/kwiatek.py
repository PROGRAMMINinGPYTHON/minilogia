global a
a = 60

def romb ():
    fillcolor("red")
    begin_fill()
    fd(a)
    rt(180+108)
    fd(a)
    rt(180)
    rt(72)
    fd(a)
    rt(180)
    lt(72)
    rt(180)
    fd(a)
    end_fill()

