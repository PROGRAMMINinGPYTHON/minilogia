from turtle import *
import math
global b
b = 100
global a
a  = b/math.sqrt(2)
def LUDZIK(n):
    linie(n)
    kształt(200, 250, a, b, pd)
    obcięcie()
    kształt(200, 250, a, b, pd)
    ręcznie()


def kształt(x,y,a,b,pen):
    pu()
    setpos(-x,-y)
    setheading(45)
    fd(a/2)
    rt(90)
    fd(a/2)
    if pen == pu:
        pu()
    else:
        pd()
    lt(90)
    fd(2*a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(2*a)
    lt(90)
    fd(a)
    lt(90)
    fd(3*a)
    rt(90)
    fd(2*a)
    lt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    lt(90)
    fd(a)
    lt(90)
    fd(a)
    rt(90)
    fd(a)
    rt(90)
    fd(a)
    lt(90)
    fd(a)
    lt(90)
    fd(2*a)
    rt(90)
    fd(3*a)
    lt(90)
    fd(a)

def linie(n):
    for i in range((n+1)*4):
        pu()
        setpos(-200, -250)
        pd()
        setheading(0)
        fd(i*(b/(n+1)))
        lt(90)
        fd(450)

def obcięcie():
    fillcolor("white")
    begin_fill()
    kształt(800,1000,a*4,b*4,pu)
    pu()
    kształt(200,250,a,b,pu)
    end_fill()

def ręcznie():
    pd()
    color("white")
    fillcolor("white")
    begin_fill()
    setheading(180)
    fd(b/2)
    rt(90)
    fd(b/2)
    rt(135)
    color("black")
    fillcolor("white")
    fd(a)
    end_fill()
    pu()
    setpos(200,-250)
    fillcolor("white")
    begin_fill()
    setheading(0)
    color('white')
    fd(2*b)
    lt(90)
    fd(3*b)
    lt(90)
    fd(2*b)
    lt(90)
    fd(3*b)
    end_fill()
    lt(90)
    color("white")
    pd()
    width(7)
    fd(3*b)
    color("red")
speed(0)
LUDZIK(3)
pu()
setpos(0,0)
pd()
done()